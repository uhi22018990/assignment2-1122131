declare(strict_types=1);

function sesto_rule_uuidv4(string $value): bool
{
  return 1 === preg_match(
    '/^[0-9A-F]{8}-[0-9A-F]{4}-4[0-9A-F]{3}-[89AB][0-9A-F]{3}-[0-9A-F]{12}$/i',
    $value
  );
}
